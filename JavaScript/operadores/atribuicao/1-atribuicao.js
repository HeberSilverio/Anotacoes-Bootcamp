//Atribuição
x = y // x esta recebendo y

// Atribuição de adição
x = x + y // x é igual a ele mesmo mais y
x += y

// Atribuição de subtração
x = x - y // x é igual a ele mesmo menos y, ou
x -= y

// Atribuição de multiplicação
x = x * y // x é igual a ele mesmo vezes y, ou
x *= y

// Atribuição de divisão
x = x / y // x é igual a ele mesmo dividido por y, ou
x /= y

// Atribuição de resto
x = x % y // x é igual a ele mesmo dividido por y com resto, ou
x %= y