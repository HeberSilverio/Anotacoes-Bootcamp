function soma(a, b){
    return a + b;
}
soma(2, 2);//o primeiro parâmetro esta sendo repetido 
soma(2, 3);
soma(2, 4);
soma(2, 5);

//Currying - Uma técnica interessante usada em programação funcional. O Currying é basicamente uma forma de informar para uma função os parâmetros de forma parcial. Você pode pegar uma função qualquer que recebe, por exemplo, três parâmetros, mandar um por vez e no fim obter o resultado.
